package com.axisdesktop.poi.helper;

public class PoiImage {
	private String path;
	private String md2sum;
	private int width;
	private int height;

	public String getPath() {
		return path;
	}

	public void setPath( String path ) {
		this.path = path;
	}

	public String getMd2sum() {
		return md2sum;
	}

	public void setMd2sum( String md2sum ) {
		this.md2sum = md2sum;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth( int width ) {
		this.width = width;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight( int height ) {
		this.height = height;
	}

}
